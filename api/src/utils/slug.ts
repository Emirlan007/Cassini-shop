export function generateSlug(text: string): string {
  let slug = text
    .split('')
    .map((char) => translitMap[char] || char)
    .join('');

  slug = slug
    .toLowerCase()
    .trim()
    .replace(/[^\w\s-]/g, '')
    .replace(/[\s_]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-+|-+$/g, '');

  return slug;
}

export function generateUniqueSlug(baseSlug: string, counter: number): string {
  return `${baseSlug}-${counter}`;
}
